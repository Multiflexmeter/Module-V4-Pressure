/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* USER CODE BEGIN SysInit */
  MX_GPIO_Init();
  variant = getVariant();
  /* Configure the system clock */
  if(variant == ONEWIRE_VARIANT)
  {
    OneWireSystemClock_Config();
    setSensorType(MFM_DRUKMODULE_ONEWIRE);
    setMeasureTime(DEF_MEAS_TIME_ONEWIRE);
  }

  else if(variant == RS485_VARIANT)
  {
    SystemClock_Config();
    setSensorType(MFM_DRUKMODULE_RS485);
    enableInitFunction(); //enable the sensor init function
    setMeasureTime(DEF_MEAS_TIME_RS485);
  }
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  MX_TIM21_Init();
  /* USER CODE BEGIN 2 */
  ModbusInit(&huart2);
  HAL_I2C_EnableListen_IT(&hi2c1);
  setSlaveAddress();

  /* USER CODE END 2 */
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    //ADC_Start(&hadc);
    switch (currentState)
    {
      case POLL_RS485_SENSOR:
        /* Measure the RS485 Sensor */
        measureKellerSensor();

        /* Finish measurement */
        currentState = SLEEP;
        break;

      case POLL_ONEWIRE_SENSOR:
        /* Measure the OneWire Sensor */
        measureHubaSensor();

        /* Finish measurement */
        currentState = SLEEP;
        break;

      case WRITE_REGISTER:
        if (regWriteData[0] == REG_SENSOR_SELECTED)
        {
          writeRegister(regWriteData, regSize + 3);
          storeSelectedSensor(regWriteData[1]);
        }
        else
          writeRegister(regWriteData, regSize + 3);

        writeFlag = false;
        currentState = SLEEP;
        break;

      case ASSIGN_ADDRESS:
        {
          bool result = true;
          if (variant == RS485_VARIANT)
          {
            result = assignAddressKeller();
          }
          setInitStatusReady(result);
          currentState = SLEEP;
        }
        break;

      case SLEEP:

        //check if a write flag is found
        if (writeFlag)
        {
          currentState = WRITE_REGISTER;
        }

        //check if a sensor init command is received
        else if( getInitStartStatus())
        {
          setInitStatusBusy();
          currentState = ASSIGN_ADDRESS;
        }

        // If the measurement start register has been set to 1
        else if(readMeasStart())
        {
          setMeasurementStatus(MEASUREMENT_ACTIVE);
          samples = readMeasSamples();

          /* Check which sensor type to poll */
          if (variant == RS485_VARIANT)
          {
            switchOnSensor_BothKeller(); //switch on timed buck converter and sensor1 + sensor2

            currentState = POLL_RS485_SENSOR;
            HAL_Delay(25); //todo remove, now 10ms at switch on + 25 = 35ms same as before
          }
          else if (variant == ONEWIRE_VARIANT)
          {
            controlBuckConverter( GPIO_PIN_SET); //enable buck converter
            HAL_Delay(2); //wait for stabilized supply
            currentState = POLL_ONEWIRE_SENSOR;
          }
        }

        //no active commands, sleep can be entered.
        else
        {
          HAL_GPIO_WritePin(SLOT_GPIO0_GPIO_Port, SLOT_GPIO0_Pin, GPIO_PIN_SET);
          enter_Sleep();
          HAL_GPIO_WritePin(SLOT_GPIO0_GPIO_Port, SLOT_GPIO0_Pin, GPIO_PIN_RESET);
        }

        break;
    }

    //check sensorSlave error is active and restart the I2C.
    if( sensorSlaveErrorFlag )
    {
      sensorSlaveErrorFlag = false;

      HAL_I2C_DeInit(&hi2c1);

      MX_I2C1_Init();
      HAL_I2C_EnableListen_IT(&hi2c1);
      setSlaveAddress();
    }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

